def generate_daily_message(day_index: int) -> str:
    """Build a deterministic daily message for the given day index (1..365)."""
    rnd = random.Random(day_index)
    template = rnd.choice(TEMPLATES)
    compliment = rnd.choice(COMPLIMENTS)
    idea = rnd.choice(IDEAS)
    signature = rnd.choice(SIGNATURES)
    return template.format(day=day_index, compliment=compliment, idea=idea, signature=signature)


def determine_special_slot(local_time: time) -> SpecialSlot:
    """Choose the special slot based on local hour."""
    hour = local_time.hour
    if 5 <= hour < 12:
        return "morning"
    if 12 <= hour < 17:
        return "day"
    if 17 <= hour < 22:
        return "evening"
    return "night"
